#!/bin/bash
# Provision an extracted airgap bundle (see scripts/dev/airgap-bundle.sh; ADR-0095)
# on a target host with no network: position the shipped toolchains, write an
# offline environment file, preflight the non-vendorable requirements, and
# (by default) build.
#
# Run it from inside the extracted tree:
#     tar -I zstd -xf boxer-airgap-*.tar.zst   # or: tar -xzf ... for the .gz form
#     boxer/scripts/dev/airgap-unpack.sh
#
# It is idempotent: re-running re-derives the env file and rebuilds.
#
#   --no-build    provision + preflight only; print how to build yourself.
#   --env-only    just (re)write boxer-airgap.env and exit.
#
# After it runs, `source boxer-airgap.env` in any shell to get an offline-
# configured Go (and, in full scope, Rust) toolchain on PATH.

set -euo pipefail

here=$(dirname "$(readlink -f "$BASH_SOURCE")")
repo=$(readlink -f "$here/../..")
cd "$repo"

mode=build
case "${1:-}" in
    --no-build) mode=no-build ;;
    --env-only) mode=env-only ;;
    "" ) ;;
    -h|--help) grep '^#' "$BASH_SOURCE" | sed 's/^# \?//'; exit 0 ;;
    *) echo "ERROR: unknown argument: $1" >&2; exit 2 ;;
esac

manifest="$repo/_airgap/MANIFEST"
[ -f "$manifest" ] || { echo "ERROR: $manifest missing — is this an extracted airgap bundle?" >&2; exit 1; }
scope="$(sed -n 's/^scope=//p' "$manifest")"
tags="$(tr -d '\n' < "$repo/tags")"

go_tc="$repo/_airgap/toolchains/go"
rust_tc="$repo/_airgap/toolchains/rust"
cargo_home="$repo/_airgap/cargo"
[ -d "$go_tc" ] || { echo "ERROR: shipped Go SDK missing at $go_tc." >&2; exit 1; }

# ---- offline environment file -----------------------------------------------
echo "=== write boxer-airgap.env ==="
{
    echo "# Sourced offline build environment for boxer. Generated by airgap-unpack.sh."
    echo "export GOROOT=\"$go_tc\""
    echo "export GOTOOLCHAIN=local"      # never try to fetch a toolchain
    echo "export GOPROXY=off"            # never reach a module proxy
    echo "export GOSUMDB=off"            # sumdb unreachable; go.sum still enforces integrity
    echo "export GOFLAGS=-mod=vendor"    # build from the shipped vendor/ tree
    echo "export CGO_ENABLED=0"          # boxer's Go build is CGO-free
    echo "export PATH=\"$go_tc/bin:\$PATH\""
    if [ "$scope" = full ]; then
        echo "export CARGO_HOME=\"$cargo_home\""
        echo "export CARGO_NET_OFFLINE=true"   # makes the repo's cargo scripts offline too
        echo "export PATH=\"$rust_tc/bin:\$PATH\""
    fi
    echo "# Point boxer's OpenAI-compatible client at the environment's ollama, e.g.:"
    echo "#   export OPENAI_BASE_URL=http://127.0.0.1:11434/v1"
} > "$repo/boxer-airgap.env"

# Materialize the cargo source-replacement config with an absolute vendor path.
if [ "$scope" = full ]; then
    [ -d "$rust_tc" ] || { echo "ERROR: full scope but Rust toolchain missing at $rust_tc." >&2; exit 1; }
    mkdir -p "$cargo_home"
    sed -E "s#^directory = .*#directory = \"$repo/rust/vendor\"#" \
        "$repo/_airgap/cargo-config.toml.in" > "$cargo_home/config.toml"
    echo "    wrote $cargo_home/config.toml (vendored sources -> $repo/rust/vendor)"
fi

[ "$mode" = env-only ] && { echo "=== env-only: done ==="; exit 0; }

# ---- preflight the things no vendoring can supply ---------------------------
echo "=== preflight (warnings only) ==="
warn() { echo "  WARN: $*" >&2; }
ok()   { echo "  ok:   $*"; }

if [ "$scope" = full ]; then
    if command -v cc >/dev/null 2>&1 || command -v gcc >/dev/null 2>&1 || command -v clang >/dev/null 2>&1
        then ok "C compiler present (needed at build time for libmimalloc-sys)"
        else warn "no C compiler (cc/gcc/clang) — the Rust build will fail on libmimalloc-sys"; fi
    command -v pkg-config >/dev/null 2>&1 \
        && ok "pkg-config present" || warn "pkg-config absent — some -sys crates probe with it"
fi

# Vulkan ICD: runtime requirement for the wgpu render head.
if command -v vulkaninfo >/dev/null 2>&1 && vulkaninfo >/dev/null 2>&1; then
    ok "Vulkan reports a device (wgpu runtime ok)"
elif { command -v ldconfig >/dev/null 2>&1 && ldconfig -p 2>/dev/null | grep -q 'libvulkan\.so\.1'; }; then
    warn "libvulkan present but no enumerable device — install an ICD (mesa-vulkan-drivers, or lavapipe for software)"
else
    warn "no Vulkan loader — imzero2's wgpu head needs libvulkan + an ICD (hardware driver or lavapipe) at runtime"
fi

# Environment-provided runtime deps (informational — not bundled by design).
for tool in clickhouse ffmpeg ollama; do
    command -v "$tool" >/dev/null 2>&1 && ok "$tool present" || warn "$tool not on PATH (expected from the environment; ollama may be a remote endpoint)"
done

[ "$mode" = no-build ] && {
    echo "=== no-build: provisioned. To build: ==="
    echo "    source boxer-airgap.env"
    echo "    go build -tags \"\$(tr -d '\\n' < tags)\" -o app ./public/app"
    [ "$scope" = full ] && echo "    ( cd rust/imzero2 && ./build_rust_headless.sh )"
    exit 0
}

# ---- build ------------------------------------------------------------------
# shellcheck disable=SC1091
source "$repo/boxer-airgap.env"

echo "=== build Go: app (aggregate CLI) ==="
go build -tags "$tags" -o "$repo/app" ./public/app
echo "=== build Go: imzero2 host (tags + binary_log, per build_go.sh) ==="
go build -tags "$tags,binary_log" -o "$repo/rust/imzero2/main_go" ./public/thestack/cmd/imzero2/

if [ "$scope" = full ]; then
    echo "=== build Rust: imzero2 headless render host (offline) ==="
    ( cd rust/imzero2 && ./build_rust_headless.sh )   # CARGO_NET_OFFLINE keeps it offline
else
    echo "=== place prebuilt imzero2 (go-only scope) ==="
    dst="rust/imzero2/target/headless/release/imzero2"
    mkdir -p "$(dirname "$dst")"
    cp "$repo/_airgap/prebuilt/imzero2" "$dst"
    echo "    installed $dst"
fi

echo "=== done. Built binaries: ==="
echo "    $repo/app                                   (aggregate CLI; try: ./app dev entry-points)"
echo "    $repo/rust/imzero2/main_go                  (imzero2 Go host)"
echo "    $repo/rust/imzero2/target/headless/release/imzero2  (Rust render head)"
echo "    Run the headless HMI with rust/imzero2/hmi_headless.sh once a Vulkan ICD is present."
