package callsites

import (
	"bufio"
	"bytes"
	"context"
	"os"
	"path/filepath"
	"regexp"
	"strconv"
	"strings"

	"github.com/stergiotis/boxer/public/extbin"
	"github.com/stergiotis/boxer/public/observability/eh/eb"
)

// posKey addresses a call site the way compiler diagnostics do: file, line,
// column of the opening parenthesis (ADR-0107 §SD1/§SD2).
type posKey struct {
	file string
	line int
	col  int
}

// reCompilerDecision matches the two `-gcflags=-m` line forms consumed here:
//
//	main.go:42:5: devirtualizing i.M to S
//	main.go:34:12: inlining call to Gen[go.shape.int]
//
// `<autogenerated>:1: …` lines carry no column and fall through the pattern.
var reCompilerDecision = regexp.MustCompile(`^(.+?):(\d+):(\d+): (devirtualizing|inlining call to) `)

// adjudicateE builds the configured patterns with -gcflags=-m and collects
// the compiler's per-position devirtualization/inlining decisions
// (ADR-0107 §SD1). A failing build is an error, not partial coverage (§SD5).
//
// singleMainRoot steers the -o handling: `go build` discards outputs for
// multi-package builds and for library packages, but a single main root
// would drop its binary into Dir — only that case needs -o diverted to a
// temp directory (`-o` with a library-only build is itself an error).
func (inst *AnalyzerService) adjudicateE(ctx context.Context, singleMainRoot bool) (decisions map[posKey]CompilerDecision, err error) {
	var baseDir string
	baseDir, err = inst.baseDirE()
	if err != nil {
		return
	}
	args := []string{"build", "-gcflags=-m"}
	if singleMainRoot {
		var outDir string
		outDir, err = os.MkdirTemp("", "callsites-adjudicate-*")
		if err != nil {
			err = eb.Build().Errorf("callsites adjudicate: temp dir: %w", err)
			return
		}
		defer func() { _ = os.RemoveAll(outDir) }()
		args = append(args, "-o", outDir)
	}
	if len(inst.BuildTags) > 0 {
		args = append(args, "-tags="+strings.Join(inst.BuildTags, ","))
	}
	args = append(args, inst.BuildFlags...)
	args = append(args, inst.patterns()...)

	cmd, cmdErr := extbin.Go.Command(ctx, extbin.Opts{Dir: inst.Dir}, args...)
	if cmdErr != nil {
		err = eb.Build().Strs("args", args).Errorf("callsites adjudicate: go build command: %w", cmdErr)
		return
	}
	var stderr bytes.Buffer
	cmd.Stderr = &stderr
	err = cmd.Run()
	if err != nil {
		err = eb.Build().
			Strs("args", args).
			Str("stderrTail", tailLines(stderr.Bytes(), 10)).
			Errorf("callsites adjudicate: go build failed: %w", err)
		return
	}
	decisions = parseCompilerDecisions(&stderr, baseDir)
	return
}

func (inst *AnalyzerService) baseDirE() (dir string, err error) {
	dir = inst.Dir
	if dir == "" {
		dir, err = os.Getwd()
		if err != nil {
			err = eb.Build().Errorf("callsites adjudicate: getwd: %w", err)
			return
		}
	}
	dir, err = filepath.Abs(dir)
	if err != nil {
		err = eb.Build().Str("dir", inst.Dir).Errorf("callsites adjudicate: absolutize: %w", err)
	}
	return
}

// parseCompilerDecisions extracts decision lines from -gcflags=-m output.
// Relative paths are resolved against baseDir. Positions that do not
// correspond to a scanned call site (decisions inside instantiated generic
// bodies report the definition's position) simply never join.
func parseCompilerDecisions(r *bytes.Buffer, baseDir string) map[posKey]CompilerDecision {
	decisions := make(map[posKey]CompilerDecision, 256)
	scanner := bufio.NewScanner(r)
	scanner.Buffer(make([]byte, 0, 64*1024), 1024*1024)
	for scanner.Scan() {
		m := reCompilerDecision.FindStringSubmatch(scanner.Text())
		if m == nil {
			continue
		}
		file := m[1]
		if !filepath.IsAbs(file) {
			file = filepath.Join(baseDir, file)
		}
		line, _ := strconv.Atoi(m[2])
		col, _ := strconv.Atoi(m[3])
		key := posKey{file: filepath.Clean(file), line: line, col: col}
		d := decisions[key]
		if m[4] == "devirtualizing" {
			d.Devirtualized = true
		} else {
			d.InlinedCall = true
		}
		decisions[key] = d
	}
	return decisions
}

func tailLines(b []byte, n int) string {
	lines := strings.Split(strings.TrimRight(string(b), "\n"), "\n")
	if len(lines) > n {
		lines = lines[len(lines)-n:]
	}
	return strings.Join(lines, "\n")
}
