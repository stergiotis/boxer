//go:build llm_generated_opus46

// Command genbuildertest generates Go test code from the SQL corpus.
//
// For each corpus entry, it:
//  1. Runs the normalization pipeline
//  2. Parses with Grammar2
//  3. Converts to AST
//  4. Calls ToGoCode() to emit builder code
//  5. Writes a test that executes the builder code and verifies the SQL parses
//
// Usage:
//
//	go run ./cmd/genbuildertest -out chsql/builder_generated_test.go
package main

import (
	"bytes"
	"flag"
	"fmt"
	"go/format"
	"log"
	"os"
	"strings"
	"unicode"

	"github.com/stergiotis/boxer/public/db/clickhouse/dsl/ast"
	"github.com/stergiotis/boxer/public/db/clickhouse/dsl/nanopass"
	"github.com/stergiotis/boxer/public/db/clickhouse/dsl/nanopass/passes"
	"github.com/stergiotis/boxer/public/db/clickhouse/dsl/nanopass/testdata"
)

func main() {
	outPath := flag.String("out", "chsql/builder_generated_test.go", "output file path")
	pkg := flag.String("pkg", "chsql_test", "package name for generated file")
	flag.Parse()

	entries, err := testdata.LoadCorpus()
	if err != nil {
		log.Fatalf("LoadCorpus: %v", err)
	}

	var buf bytes.Buffer

	buf.WriteString("// Code generated by genbuildertest; DO NOT EDIT.\n")
	buf.WriteString("//\n")
	buf.WriteString("// Regenerate with:\n")
	buf.WriteString("//   go generate ./...\n")
	buf.WriteString("//\n")
	buf.WriteString("// Each test reconstructs a corpus query using the chsql builder API,\n")
	buf.WriteString("// then verifies the resulting SQL is parseable by Grammar1.\n\n")
	fmt.Fprintf(&buf, "package %s\n\n", *pkg)
	buf.WriteString(`
import (
	"testing"

	"github.com/stergiotis/boxer/public/db/clickhouse/dsl/ast"
	. "github.com/stergiotis/boxer/public/db/clickhouse/dsl/astbuilder"
	"github.com/stergiotis/boxer/public/db/clickhouse/dsl/nanopass"
	"github.com/stretchr/testify/require"
)
`)

	// Suppress unused import warnings
	buf.WriteString("var _ ast.ExprKind\n\n")

	// Helper for taking address of SelectUnion in struct literals.
	// Accepts a SelectBuilder, calls Build(), and returns &Body.
	buf.WriteString("func buildUnionPtr(sb SelectBuilder) *ast.SelectUnion {\n")
	buf.WriteString("\tq, _ := sb.Build()\n")
	buf.WriteString("\treturn &q.Body\n")
	buf.WriteString("}\n\n")

	passed := 0
	skipped := 0

	for _, entry := range entries {
		normalized, err := fullPipeline(entry.SQL)
		if err != nil {
			log.Printf("SKIP %s: pipeline: %v", entry.Name, err)
			skipped++
			continue
		}

		pr, err := nanopass.ParseCanonical(normalized)
		if err != nil {
			log.Printf("SKIP %s: ParseCanonical: %v", entry.Name, err)
			skipped++
			continue
		}

		query, err := ast.ConvertCSTToAST(pr)
		if err != nil {
			log.Printf("SKIP %s: ConvertCSTToAST: %v", entry.Name, err)
			skipped++
			continue
		}

		goCode := query.ToGoCode()

		testName := sanitizeTestName(entry.Name)

		fmt.Fprintf(&buf, "// Source: %s\n", entry.Name)
		fmt.Fprintf(&buf, "// SQL: %s\n", oneLine(entry.SQL))
		fmt.Fprintf(&buf, "func TestGenerated_%s(t *testing.T) {\n", testName)
		fmt.Fprintf(&buf, "\tq, err := %s.\n\t\tBuild()\n", goCode)
		buf.WriteString("\trequire.NoError(t, err, \"builder error\")\n")
		buf.WriteString("\tsql := q.ToSQL()\n")
		buf.WriteString("\t_, err = nanopass.Parse(sql)\n")
		fmt.Fprintf(&buf, "\trequire.NoError(t, err, \"generated SQL not parseable:\\n%%s\", sql)\n")
		buf.WriteString("}\n\n")

		passed++
	}

	log.Printf("Generated %d tests, skipped %d (of %d corpus entries)", passed, skipped, len(entries))

	// Format the generated Go code
	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		// Write unformatted for debugging
		os.WriteFile(*outPath+".raw", buf.Bytes(), 0644)
		log.Fatalf("gofmt failed (raw output written to %s.raw): %v", *outPath, err)
	}

	err = os.WriteFile(*outPath, formatted, 0644)
	if err != nil {
		log.Fatalf("WriteFile: %v", err)
	}

	log.Printf("Wrote %s (%d bytes)", *outPath, len(formatted))
}

func fullPipeline(sql string) (result string, err error) {
	return passes.CanonicalizeFull(100)(sql)
}

func sanitizeTestName(name string) string {
	var b strings.Builder
	for _, r := range name {
		if unicode.IsLetter(r) || unicode.IsDigit(r) {
			b.WriteRune(r)
		} else {
			b.WriteByte('_')
		}
	}
	s := b.String()
	// Ensure starts with letter
	if len(s) > 0 && unicode.IsDigit(rune(s[0])) {
		s = "T" + s
	}
	return s
}

func oneLine(s string) string {
	s = strings.ReplaceAll(s, "\n", " ")
	s = strings.ReplaceAll(s, "\t", " ")
	// Collapse multiple spaces
	for strings.Contains(s, "  ") {
		s = strings.ReplaceAll(s, "  ", " ")
	}
	s = strings.TrimSpace(s)
	if len(s) > 120 {
		s = s[:117] + "..."
	}
	return s
}
