package genbuildertest

import (
	"bytes"
	"fmt"
	"go/format"
	"os"
	"strings"
	"unicode"

	"github.com/rs/zerolog/log"
	"github.com/stergiotis/boxer/public/db/clickhouse/dsl/ast"
	"github.com/stergiotis/boxer/public/db/clickhouse/dsl/nanopass"
	"github.com/stergiotis/boxer/public/db/clickhouse/dsl/nanopass/passes"
	"github.com/stergiotis/boxer/public/db/clickhouse/dsl/nanopass/testdata"
	"github.com/stergiotis/boxer/public/observability/eh/eb"
)

// Run walks the chsql corpus and writes generated builder-roundtrip
// tests to outPath under the supplied Go package name. Returns an
// error only for I/O / formatting failures; per-entry pipeline
// failures are logged as skips and do not abort the run.
func Run(outPath, pkg string) (err error) {
	entries, err := testdata.LoadCorpus()
	if err != nil {
		err = eb.Build().Errorf("LoadCorpus: %w", err)
		return
	}

	var buf bytes.Buffer

	buf.WriteString("// Code generated by boxer code gen-builder-tests; DO NOT EDIT.\n")
	buf.WriteString("//\n")
	buf.WriteString("// Regenerate with:\n")
	buf.WriteString("//   go generate ./...\n")
	buf.WriteString("//\n")
	buf.WriteString("// Each test reconstructs a corpus query using the chsql builder API,\n")
	buf.WriteString("// then verifies the resulting SQL is parseable by Grammar1.\n\n")
	fmt.Fprintf(&buf, "package %s\n\n", pkg)
	buf.WriteString(`
import (
	"testing"

	"github.com/stergiotis/boxer/public/db/clickhouse/dsl/ast"
	. "github.com/stergiotis/boxer/public/db/clickhouse/dsl/astbuilder"
	"github.com/stergiotis/boxer/public/db/clickhouse/dsl/nanopass"
	"github.com/stretchr/testify/require"
)
`)

	buf.WriteString("var _ ast.ExprKind\n\n")

	// buildUnionPtr lets a caller embed a SelectBuilder where a
	// *SelectUnion is required (struct literals in subquery position).
	buf.WriteString("func buildUnionPtr(sb SelectBuilder) *ast.SelectUnion {\n")
	buf.WriteString("\tq, _ := sb.Build()\n")
	buf.WriteString("\treturn &q.Body\n")
	buf.WriteString("}\n\n")

	passed := 0
	skipped := 0

	for _, entry := range entries {
		normalized, pErr := fullPipeline(entry.SQL)
		if pErr != nil {
			log.Info().Str("name", entry.Name).Err(pErr).Msg("skip: pipeline failure")
			skipped++
			continue
		}

		pr, pErr := nanopass.ParseCanonical(normalized)
		if pErr != nil {
			log.Info().Str("name", entry.Name).Err(pErr).Msg("skip: ParseCanonical failure")
			skipped++
			continue
		}

		query, pErr := ast.ConvertCSTToAST(pr)
		if pErr != nil {
			log.Info().Str("name", entry.Name).Err(pErr).Msg("skip: ConvertCSTToAST failure")
			skipped++
			continue
		}

		goCode := query.ToGoCode()
		testName := sanitizeTestName(entry.Name)

		fmt.Fprintf(&buf, "// Source: %s\n", entry.Name)
		fmt.Fprintf(&buf, "// SQL: %s\n", oneLine(entry.SQL))
		fmt.Fprintf(&buf, "func TestGenerated_%s(t *testing.T) {\n", testName)
		fmt.Fprintf(&buf, "\tq, err := %s.\n\t\tBuild()\n", goCode)
		buf.WriteString("\trequire.NoError(t, err, \"builder error\")\n")
		buf.WriteString("\tsql := q.ToSQL()\n")
		buf.WriteString("\t_, err = nanopass.Parse(sql)\n")
		fmt.Fprintf(&buf, "\trequire.NoError(t, err, \"generated SQL not parseable:\\n%%s\", sql)\n")
		buf.WriteString("}\n\n")

		passed++
	}

	log.Info().Int("passed", passed).Int("skipped", skipped).Int("total", len(entries)).Msg("gen-builder-tests: corpus processed")

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		// Write the unformatted output for debugging so the gofmt failure can
		// be inspected against the raw emission.
		_ = os.WriteFile(outPath+".raw", buf.Bytes(), 0o644)
		err = eb.Build().Str("rawPath", outPath+".raw").Errorf("gofmt failed: %w", err)
		return
	}

	err = os.WriteFile(outPath, formatted, 0o644)
	if err != nil {
		err = eb.Build().Str("outPath", outPath).Errorf("WriteFile: %w", err)
		return
	}

	log.Info().Str("outPath", outPath).Int("bytes", len(formatted)).Msg("gen-builder-tests: wrote output")
	return
}

func fullPipeline(sql string) (result string, err error) {
	result, err = passes.CanonicalizeFull(100).Run(sql)
	return
}

func sanitizeTestName(name string) (out string) {
	var b strings.Builder
	for _, r := range name {
		if unicode.IsLetter(r) || unicode.IsDigit(r) {
			b.WriteRune(r)
		} else {
			b.WriteByte('_')
		}
	}
	s := b.String()
	if len(s) > 0 && unicode.IsDigit(rune(s[0])) {
		s = "T" + s
	}
	out = s
	return
}

func oneLine(s string) (out string) {
	s = strings.ReplaceAll(s, "\n", " ")
	s = strings.ReplaceAll(s, "\t", " ")
	for strings.Contains(s, "  ") {
		s = strings.ReplaceAll(s, "  ", " ")
	}
	s = strings.TrimSpace(s)
	if len(s) > 120 {
		s = s[:117] + "..."
	}
	out = s
	return
}
