// Package text2sql provides natural language to ClickHouse SQL translation
// using a local Ollama LLM with structural validation feedback.
//
// The LLM generates SQL freely. The nanopass pipeline validates and
// canonicalizes the output. On failure, the error is fed back for retry.
//
// Usage:
//
//	gen := text2sql.New("http://localhost:11434", "qwen2.5-coder:7b", schema)
//	result, err := gen.Generate(ctx, "count orders by country for 2024")
//	if err != nil { ... }
//	fmt.Println(result.SQL)       // canonical SQL
//	fmt.Println(result.AST)       // ast.Query
//	fmt.Println(result.Attempts)  // number of LLM calls
package text2sql

import (
	"bytes"
	"context"
	"encoding/json"
	"fmt"
	"io"
	"net/http"
	"strings"
	"time"

	ast "github.com/stergiotis/boxer/public/db/clickhouse/dsl/ast"
	"github.com/stergiotis/boxer/public/db/clickhouse/dsl/nanopass"
	"github.com/stergiotis/boxer/public/db/clickhouse/dsl/nanopass/passes"
	"github.com/stergiotis/boxer/public/observability/eh"
	"github.com/stergiotis/boxer/public/observability/eh/eb"
)

// Result is the output of a successful text-to-SQL generation.
type Result struct {
	SQL         string    // canonical (Grammar2-compliant) SQL
	AST         ast.Query // parsed and converted AST
	RawSQL      string    // SQL as generated by the LLM (before normalization)
	Attempts    int       // number of LLM calls (1 = first try)
	Model       string    // model used
	TotalTokens int       // approximate total tokens across all attempts
}

// SchemaTable describes a table for the LLM prompt.
type SchemaTable struct {
	Database string
	Table    string
	Columns  []SchemaColumn
	Comment  string // optional human-readable description
}

// SchemaColumn describes a column.
type SchemaColumn struct {
	Name    string
	Type    string
	Comment string // optional human-readable description
}

// Generator translates natural language to ClickHouse SQL.
type Generator struct {
	endpoint     string
	model        string
	schema       []SchemaTable
	maxAttempts  int
	httpCli      *http.Client
	systemPrompt string
}

// Option configures a Generator.
type Option func(*Generator)

// WithMaxAttempts sets the maximum number of LLM calls (default: 3).
func WithMaxAttempts(n int) Option {
	return func(g *Generator) { g.maxAttempts = n }
}

// WithSystemPromptOverride replaces the default system prompt entirely.
func WithSystemPromptOverride(prompt string) Option {
	return func(g *Generator) { g.systemPrompt = prompt }
}

// New creates a Generator.
func New(endpoint, model string, schema []SchemaTable, opts ...Option) *Generator {
	g := &Generator{
		endpoint:    strings.TrimRight(endpoint, "/"),
		model:       model,
		schema:      schema,
		maxAttempts: 3,
		httpCli:     &http.Client{Timeout: 120 * time.Second},
	}
	for _, opt := range opts {
		opt(g)
	}
	if g.systemPrompt == "" {
		g.systemPrompt = g.buildSystemPrompt()
	}
	return g
}

// Generate translates a natural language question to ClickHouse SQL.
func (inst *Generator) Generate(ctx context.Context, question string) (result Result, err error) {
	result.Model = inst.model

	messages := []ollamaMessage{
		{Role: "system", Content: inst.systemPrompt},
		{Role: "user", Content: question},
	}

	for attempt := 1; attempt <= inst.maxAttempts; attempt++ {
		result.Attempts = attempt

		var resp ollamaResponse
		resp, err = inst.chat(ctx, messages)
		if err != nil {
			err = eb.Build().Int("attempt", attempt).Errorf("ollama request failed: %w", err)
			return
		}
		result.TotalTokens += resp.EvalCount + resp.PromptEvalCount

		rawSQL := extractSQL(resp.Message.Content)
		result.RawSQL = rawSQL

		// Try to validate and canonicalize
		var validationErr error
		result.SQL, result.AST, validationErr = inst.validate(rawSQL)

		if validationErr == nil {
			return
		}

		// Validation failed — feed error back to LLM for retry
		if attempt < inst.maxAttempts {
			messages = append(messages,
				ollamaMessage{Role: "assistant", Content: resp.Message.Content},
				ollamaMessage{Role: "user", Content: formatFeedback(rawSQL, validationErr)},
			)
		} else {
			err = eb.Build().Int("attempts", attempt).Errorf("all attempts failed: %w", validationErr)
		}
	}
	return
}

// validate runs the full pipeline on raw SQL.
func (inst *Generator) validate(rawSQL string) (canonical string, query ast.Query, err error) {
	// Step 1: Grammar1 parse
	_, err = nanopass.Parse(rawSQL)
	if err != nil {
		err = eh.Errorf("SQL syntax error: %w", err)
		return
	}

	// Step 2: Normalize
	canonical, err = fullPipeline(rawSQL)
	if err != nil {
		err = eh.Errorf("normalization error: %w", err)
		return
	}

	// Step 3: Grammar2 parse (canonical validation)
	pr, parseErr := nanopass.ParseCanonical(canonical)
	if parseErr != nil {
		err = eh.Errorf("canonical validation error: %w", parseErr)
		return
	}

	// Step 4: AST conversion
	query, err = ast.ConvertCSTToAST(pr)
	if err != nil {
		err = eh.Errorf("AST conversion error: %w", err)
		return
	}

	return
}

func fullPipeline(sql string) (string, error) {
	return passes.CanonicalizeFull(100)(sql)
}

// ============================================================================
// Prompt construction
// ============================================================================

func (inst *Generator) buildSystemPrompt() string {
	var b strings.Builder

	trippleTick := "```"
	fmt.Fprintf(&b, `You are a ClickHouse SQL expert. Your task is to translate natural language questions into ClickHouse SELECT queries.

RULES:
- Output ONLY a single SELECT query, no explanations
- Wrap the SQL in a %ssql code block
	- Use only the tables and columns provided in the schema below
	- Use standard ClickHouse SQL syntax
	- Use double quotes for identifiers that need quoting
	- Use single = for equality (not ==)
	- Use if() instead of CASE WHEN for simple conditions
	- Use multiIf() for multi-branch conditions
	- Do not use CAST(expr AS Type), use CAST(expr, 'Type') instead
	- Do not use :: for type casting
	- Do not use array or tuple literal syntax [1,2,3] or (1,2,3) — use array() and tuple()
- Always include a FROM clause when querying tables
- Prefer explicit JOIN ... ON over comma joins
%s
`, trippleTick, trippleTick)

	b.WriteString("SCHEMA:\n")
	for _, table := range inst.schema {
		if table.Database != "" {
			fmt.Fprintf(&b, "\nTable: %s.%s", table.Database, table.Table)
		} else {
			fmt.Fprintf(&b, "\nTable: %s", table.Table)
		}
		if table.Comment != "" {
			fmt.Fprintf(&b, " — %s", table.Comment)
		}
		b.WriteString("\nColumns:\n")
		for _, col := range table.Columns {
			fmt.Fprintf(&b, "  - %s %s", col.Name, col.Type)
			if col.Comment != "" {
				fmt.Fprintf(&b, " — %s", col.Comment)
			}
			b.WriteByte('\n')
		}
	}

	return b.String()
}

func formatFeedback(rawSQL string, validationErr error) string {
	var b strings.Builder
	b.WriteString("Your SQL has an error. Please fix it and output only the corrected SQL in a ```sql code block.\n\n")
	b.WriteString("Your SQL:\n```sql\n")
	b.WriteString(rawSQL)
	b.WriteString("\n```\n\n")
	b.WriteString("Error:\n")
	b.WriteString(validationErr.Error())
	b.WriteString("\n")
	return b.String()
}

// extractSQL pulls SQL from a markdown code block, or returns the raw text.
func extractSQL(response string) string {
	// Try ```sql ... ``` block
	if idx := strings.Index(response, "```sql"); idx >= 0 {
		start := idx + 6
		// Skip optional newline after ```sql
		if start < len(response) && response[start] == '\n' {
			start++
		}
		end := strings.Index(response[start:], "```")
		if end >= 0 {
			return strings.TrimSpace(response[start : start+end])
		}
	}
	// Try plain ``` ... ``` block
	if idx := strings.Index(response, "```"); idx >= 0 {
		start := idx + 3
		if start < len(response) && response[start] == '\n' {
			start++
		}
		end := strings.Index(response[start:], "```")
		if end >= 0 {
			return strings.TrimSpace(response[start : start+end])
		}
	}
	// No code block — try to find SELECT
	lines := strings.Split(response, "\n")
	var sqlLines []string
	inSQL := false
	for _, line := range lines {
		trimmed := strings.TrimSpace(line)
		upper := strings.ToUpper(trimmed)
		if !inSQL && (strings.HasPrefix(upper, "SELECT") || strings.HasPrefix(upper, "WITH")) {
			inSQL = true
		}
		if inSQL {
			if trimmed == "" && len(sqlLines) > 0 {
				break
			}
			sqlLines = append(sqlLines, line)
		}
	}
	if len(sqlLines) > 0 {
		return strings.TrimSpace(strings.Join(sqlLines, "\n"))
	}
	return strings.TrimSpace(response)
}

// ============================================================================
// Ollama HTTP API
// ============================================================================

type ollamaMessage struct {
	Role    string `json:"role"`
	Content string `json:"content"`
}

type ollamaRequest struct {
	Model    string          `json:"model"`
	Messages []ollamaMessage `json:"messages"`
	Stream   bool            `json:"stream"`
	Options  *ollamaOptions  `json:"options,omitempty"`
}

type ollamaOptions struct {
	Temperature float64 `json:"temperature"`
	NumPredict  int     `json:"num_predict,omitempty"`
}

type ollamaResponse struct {
	Message         ollamaMessage `json:"message"`
	EvalCount       int           `json:"eval_count"`
	PromptEvalCount int           `json:"prompt_eval_count"`
}

func (inst *Generator) chat(ctx context.Context, messages []ollamaMessage) (resp ollamaResponse, err error) {
	reqBody := ollamaRequest{
		Model:    inst.model,
		Messages: messages,
		Stream:   false,
		Options:  &ollamaOptions{Temperature: 0.1},
	}

	bodyBytes, err := json.Marshal(reqBody)
	if err != nil {
		err = eh.Errorf("marshal: %w", err)
		return
	}

	req, err := http.NewRequestWithContext(ctx, http.MethodPost, inst.endpoint+"/api/chat", bytes.NewReader(bodyBytes))
	if err != nil {
		err = eh.Errorf("request: %w", err)
		return
	}
	req.Header.Set("Content-Type", "application/json")

	httpResp, err := inst.httpCli.Do(req)
	if err != nil {
		err = eh.Errorf("do: %w", err)
		return
	}
	defer httpResp.Body.Close()

	respBytes, err := io.ReadAll(httpResp.Body)
	if err != nil {
		err = eh.Errorf("read: %w", err)
		return
	}

	if httpResp.StatusCode != http.StatusOK {
		err = eb.Build().Int("statusCode", httpResp.StatusCode).Str("body", string(respBytes)).Errorf("ollama HTTP error")
		return
	}

	err = json.Unmarshal(respBytes, &resp)
	if err != nil {
		err = eh.Errorf("unmarshal: %w", err)
		return
	}

	return
}
