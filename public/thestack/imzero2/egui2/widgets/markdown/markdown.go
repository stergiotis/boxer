// Doc is a parsed Obsidian-flavored markdown document. Construct with
// [Parse]; render once or many times via [Doc.Render]. Doc is immutable
// after construction and safe to share across goroutines provided the
// frame-time render path remains single-threaded.
//
// imageMaxW / imageMaxH cap the bounding box for FitAspectMaxE on
// inline images sourced from [resolver.ResolverI.LoadImage]. Pixels
// are decoded once at parse time, stashed on a runKindImage paragraph
// run, and re-sent every frame; the bindings doc-comment at
// [c.ImageVersionTracker] explicitly recommends skipping the tracker
// for static assets ("the per-widget-id one-shot upload cost is
// negligible") because per-widget-id tracking would silently break
// the package-level `var doc = markdown.Parse(...)` retain-once /
// render-many idiom when the same Doc is rendered under multiple id
// scopes.
type Doc struct {
	segments    []segment
	frontmatter *containers.BinarySearchGrowingKV[string, interface{}]
	headings    []HeadingInfo
	imageMaxW   uint32
	imageMaxH   uint32
}

// HeadingInfo describes one top-level heading discovered during [Parse].
// Slug is generated by lower-casing Text and replacing ' ' with '-',
// matching boxer's [resolver.NoopResolver] fragment sanitiser so
// wikilink targets and section anchors collide on the same key. Level
// is 1..6 (CommonMark ATX heading depth).
//
// HeadingInfo is what callers feed to a help/TOC sidebar or a section
// jump table; the inline styling (bold/italic/code spans inside the
// heading) is intentionally flattened to plain text.
type HeadingInfo struct {
	Text  string
	Slug  string
	Level uint8
}

// Frontmatter returns the YAML frontmatter parsed from the document
// as a key-sorted KV. Iteration via IteratePairs is deterministic
// across frames (Go's map iteration is not, which would otherwise
// re-shuffle frontmatter UI every frame).
//
// Returns nil when frontmatter is disabled (see [WithFeatures]) or
// when the source contains none.
func (inst *Doc) Frontmatter() (fm *containers.BinarySearchGrowingKV[string, interface{}]) {
	fm = inst.frontmatter
	return
}

// Headings returns the top-level headings discovered during [Parse] in
// document order. The returned slice is owned by Doc; callers that
// mutate it MUST copy first.
//
// The slice is empty for documents without headings. Use this to build
// a table-of-contents sidebar or to resolve in-doc section anchors
// (`#section-slug`) to display titles.
func (inst *Doc) Headings() (headings []HeadingInfo) {
	headings = inst.headings
	return
}

// SlugHeading returns the URL-fragment slug for a heading text, matching
// boxer's [resolver.NoopResolver] sanitiser: lower-cased, ' ' replaced
// by '-'. Exported so help/TOC consumers compose link targets that
// resolve against the same fragment keys [Doc.Headings] populates.
func SlugHeading(text string) (slug string) {
	slug = strings.ToLower(text)
	slug = strings.ReplaceAll(slug, " ", "-")
	return
}

// Render emits the document into the current egui Ui scope, deriving
// ids for embedded widgets (code blocks, blockquotes, callouts, inline
// images) from ids.PrepareSeq(0), PrepareSeq(1), … in document order.
//
// Render does NOT open its own [bindings.IdScope]. Callers that may
// render multiple instances of any markdown doc under the same parent
// scope MUST wrap the call:
//
//	for range c.IdScope(ids.PrepareStr("help-section")) {
//	    helpDoc.Render(ids)
//	}
//
// Rendering inside a layout container that owns its own id context (a
// [bindings.CollapsingHeader] body, [bindings.Window] body, etc.)
// usually does not need an extra wrap — the outer container already
// scopes the ids.
func (inst *Doc) Render(ids *c.WidgetIdStack, opts ...RenderOpt) {
	inst.renderCollect(ids, "", false, opts)
}

// CodeBlockAction is one code/verbatim block whose action button was
// clicked during a [Doc.RenderActions] frame. Text is the block's
// verbatim source (what the author wrote, before the highlighter's
// canonicalisation); Lang is the normalised fence language ("go", "sql",
// "" for an unlabelled/indented block); Index is the block's 0-based
// ordinal among the code blocks in the document, stable across frames.
type CodeBlockAction struct {
	Text  string
	Lang  string
	Index int
}

// RenderActions renders the document like [Doc.Render] but additionally
// places a small button labelled `label` above every code/verbatim
// block, and returns the blocks whose button was clicked this frame.
//
// It is an immediate-mode API: the whole document is drawn eagerly when
// RenderActions is called (the returned sequence merely replays clicks
// already captured), so ranging over it, breaking early, or ignoring it
// entirely all leave the same pixels on screen. A single frame can yield
// more than one action when the user clicks several buttons before the
// next paint, so the sequence may produce zero, one, or many values.
//
// Typical use — wire the button to any per-block action:
//
//	for act := range doc.RenderActions(ids, "Copy", markdown.WithScrollToSection(s)) {
//		text := act.Text
//		go func() { _, _ = bus.Request(clipboardbroker.SubjectWrite, []byte(text)) }()
//	}
//
// The renderer itself performs no action — it only reports clicks; the
// caller decides what a click means. CodeView's built-in selectable text
// (Ctrl+C) is unaffected and present whether or not buttons are shown.
func (inst *Doc) RenderActions(ids *c.WidgetIdStack, label string, opts ...RenderOpt) iter.Seq[CodeBlockAction] {
	actions := inst.renderCollect(ids, label, true, opts)
	return slices.Values(actions)
}

// renderCollect is the shared render core. actionsEnabled gates the
// per-code-block action button (labelled actionLabel); when true the
// returned slice holds every block whose button was clicked this frame,
// in document order. [Doc.Render] calls it with actions disabled (nil
// return); [Doc.RenderActions] wraps the slice as an iter.Seq.
func (inst *Doc) renderCollect(ids *c.WidgetIdStack, actionLabel string, actionsEnabled bool, opts []RenderOpt) (actions []CodeBlockAction) {
	var ro renderOptions
	for _, opt := range opts {
		opt(&ro)
	}
	rc := renderCtx{
		ids:            ids,
		imageMaxW:      inst.imageMaxW,
		imageMaxH:      inst.imageMaxH,
		scrollToSlug:   ro.scrollToSlug,
		headings:       inst.headings,
		actionsEnabled: actionsEnabled,
		actionLabel:    actionLabel,
	}
	for i := range inst.segments {
		inst.segments[i].render(&rc)
	}
	actions = rc.codeActions
	return
}

// RenderOpt configures a single [Doc.Render] call. Construct via the
// `With*` helpers; passing options to one call does not affect later
// calls — every Render starts from defaults.
type RenderOpt func(*renderOptions)

type renderOptions struct {
	scrollToSlug string
}

// WithScrollToSection asks the next [Doc.Render] to schedule an egui
// ScrollArea scroll-to-top exactly once, when the heading whose
// [SlugHeading] matches slug is about to render. Empty slug is a
// no-op. The underlying [bindings.ScrollToCursor] op is silently
// dropped when the doc renders outside a ScrollArea, so passing this
// hint is safe even in tooltip / popup contexts that don't scroll.
//
// Callers are expected to gate this option on their own
// "selection-just-changed" signal — re-passing the same slug every
// frame keeps re-scrolling and prevents the user from scrolling
// away. See helphost.HelpHost for the typical "last-scrolled vs
// currently-selected" guard.
func WithScrollToSection(slug string) (opt RenderOpt) {
	opt = func(o *renderOptions) {
		o.scrollToSlug = slug
	}
	return
}

// renderCtx threads the per-Render-invocation id sequence counter and
// the configured fit cap through the recursive segment walk. A single
// instance is allocated per Render call and threaded by pointer;
// idSeq is bumped at each id-needing widget.
type renderCtx struct {
	ids       *c.WidgetIdStack
	idSeq     uint64
	imageMaxW uint32
	imageMaxH uint32

	// Scroll-to-section state. scrollToSlug is the caller-provided
	// target ([WithScrollToSection]); empty disables the dispatch.
	// headings mirrors [Doc.headings] for slug→ordinal lookup at
	// render time, and headingIdx is bumped as the walker emits each
	// heading segment so the dispatch matches against the correct
	// occurrence of the slug when a doc reuses a heading text.
	scrollToSlug string
	headings     []HeadingInfo
	headingIdx   int

	// Code-block action state ([Doc.RenderActions]). When actionsEnabled
	// is set, each code block emits a small button labelled actionLabel;
	// a click appends a [CodeBlockAction] to codeActions. codeBlockIdx is
	// bumped per code block so each action carries a stable 0-based
	// ordinal. All three are zero/empty under the plain [Doc.Render] path.
	actionsEnabled bool
	actionLabel    string
	codeActions    []CodeBlockAction
	codeBlockIdx   int
}

// Option configures [Parse]. Pass options at construction time.
type Option func(*config)

type config struct {
	features  obsidian.FeatureE
	resolver  resolver.ResolverI
	imageMaxW uint32
	imageMaxH uint32
}

// imageMaxDefaultW / imageMaxDefaultH are the FitAspectMaxE bounding box
// used when WithImageMaxSize is not supplied. Tuned for typical
// embedded screenshots and asset previews in a sidebar or panel layout;
// callers driving full-width content (e.g. a Markdown reader window)
// should override.
const (
	imageMaxDefaultW uint32 = 800
	imageMaxDefaultH uint32 = 600
)

func defaultConfig() (cfg config) {
	cfg.features = obsidian.FeatureFrontmatter |
		obsidian.FeatureGFM |
		obsidian.FeatureWikilink |
		obsidian.FeatureEmbed |
		obsidian.FeatureCallout |
		obsidian.FeatureHighlight |
		obsidian.FeatureComment
	cfg.resolver = resolver.NoopResolver{}
	cfg.imageMaxW = imageMaxDefaultW
	cfg.imageMaxH = imageMaxDefaultH
	return
}

// WithFeatures overrides the default obsidian feature set. The default
// covers everything the renderer can lower in phase 2: frontmatter,
// GFM (tables / strikethrough / footnotes / task lists), wikilinks,
// embeds, callouts, ==highlight== and %%comment%% stripping. Tables,
// images and math remain deferred — the parser recognises them but the
// renderer drops them.
func WithFeatures(features obsidian.FeatureE) (opt Option) {
	opt = func(cfg *config) {
		cfg.features = features
	}
	return
}

// WithResolver supplies a custom wikilink/embed resolver. Defaults to
// [resolver.NoopResolver] which generates `/page#heading` URLs without
// vault knowledge. Vault-aware UIs implement [resolver.ResolverI] and
// pass it via this option so wikilinks land on real notes.
//
// The resolver's LoadImage method is consulted at parse time to decode
// inline images (CommonMark `![alt](url)` and Obsidian `![[file.png]]`)
// into RGBA8 pixels. NoopResolver returns ok=false from LoadImage, so
// the default behaviour is the pre-image-widget glyph-hyperlink
// fallback. Supply a vault-aware resolver to opt into real inline
// image rendering.
func WithResolver(r resolver.ResolverI) (opt Option) {
	opt = func(cfg *config) {
		if r != nil {
			cfg.resolver = r
		}
	}
	return
}

// WithImageMaxSize caps the bounding box used when rendering inline
// images with FitAspectMaxE — images are scaled aspect-preserving to
// fit inside (maxW × maxH). Zero in either dimension is treated as
// "no cap on that axis" (FitAspectMaxE then degenerates to a one-axis
// fit). Defaults are (800, 600).
func WithImageMaxSize(maxW uint32, maxH uint32) (opt Option) {
	opt = func(cfg *config) {
		cfg.imageMaxW = maxW
		cfg.imageMaxH = maxH
	}
	return
}

// Parse parses obsidian-flavored markdown and returns a renderable
// [Doc]. Hoist the result to a package-level var to amortise parse cost
// across many render frames — it mirrors the
// [codeview.PrepareSql] retain-once / render-many pattern.
func Parse(md []byte, opts ...Option) (doc *Doc) {
	cfg := defaultConfig()
	for _, opt := range opts {
		opt(&cfg)
	}

	segments, frontmatter, headings := parseAndLower(md, &cfg)
	doc = &Doc{
		segments:    segments,
		frontmatter: frontmatter,
		headings:    headings,
		imageMaxW:   cfg.imageMaxW,
		imageMaxH:   cfg.imageMaxH,
	}
	return
}

// segKindE tags a segment in the lowered document tree.
type segKindE uint8

const (
	segKindParagraph segKindE = iota
	segKindHeading
	segKindCodeBlock
	segKindList
	segKindListItem
	segKindBlockquote
	segKindHorizontalRule
	segKindCallout
)

// runKindE tags a run inside a paragraph or heading segment.
type runKindE uint8

const (
	runKindAtoms runKindE = iota
	runKindLink
	// runKindImage carries pre-decoded RGBA8 pixels plus native
	// dimensions, sourced from resolver.LoadImage at parse time. Rendered
	// inline inside the paragraph's HorizontalWrapped flow via c.Image
	// with FitAspectMaxE and the [Doc]'s configured cap.
	runKindImage
)

// paragraphRun is one slice of a paragraph or heading. A paragraph with
// no embedded hyperlinks has exactly one runKindAtoms run; one with
// hyperlinks (or images) has alternating atoms / link / image runs in
// flow order.
type paragraphRun struct {
	kind  runKindE
	atoms typed.RetainedFffiHolderTyped[c.AtomsS]
	label string
	url   string

	// runKindImage fields. imgPixels is row-major RGBA8 in
	// (imgHeightPx × imgWidthPx) order. Alt-text / Title are not
	// carried — egui's Image widget exposes no tooltip slot through
	// our binding today, so storing them would be premature.
	imgPixels   []uint32
	imgWidthPx  uint32
	imgHeightPx uint32
}

// segment is one node of the lowered document tree. The interpretation
// of each field depends on kind:
//
//   - segKindParagraph / segKindHeading: runs is the inline flow.
//   - segKindCodeBlock:                  code holds the retained job;
//     codeText holds the verbatim source
//     and codeLang the fence language,
//     both surfaced by [Doc.RenderActions].
//   - segKindList:                       children is a slice of segKindListItem
//     segments; listOrdered + listStart
//     drive the bullet glyph.
//   - segKindListItem:                   children is the item's nested blocks.
//   - segKindBlockquote:                 children is the quoted blocks.
//   - segKindHorizontalRule:             no payload.
//   - segKindCallout:                    children is the inner blocks; the
//     callout* fields drive theme,
//     title and the foldable variant.
type segment struct {
	kind               segKindE
	runs               []paragraphRun
	code               typed.RetainedFffiHolderTyped[c.CodeViewJobS]
	codeText           string
	codeLang           string
	children           []segment
	listOrdered        bool
	listStart          uint32
	calloutType        string
	calloutTitle       string
	calloutFoldable    bool
	calloutDefaultOpen bool
}
