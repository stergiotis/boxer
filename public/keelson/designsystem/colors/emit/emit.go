// Package emit writes the four generator artefacts:
//   - palette_generated.rs (Rust Color32 consts)
//   - palette_generated.go (Go egui-equivalent consts)
//   - color.md (human-readable spec + contrast table)
//   - ip-boundary-check.md (verbatim-search log)
package emit

import (
	"fmt"
	"sort"
	"strings"
	"time"

	"github.com/stergiotis/boxer/public/keelson/designsystem/colors/contrast"
	"github.com/stergiotis/boxer/public/keelson/designsystem/colors/ipboundary"
	"github.com/stergiotis/boxer/public/keelson/designsystem/colors/palette"
)

const generatedHeader = `// Code generated by ./public/app/commands/designsystem colors gen — DO NOT EDIT.
// Source-of-truth: rust/imzero2/assets/colors/palette.toml
// Generator: ADR-0033 §SD5; semantic palette per ADR-0031 §SD2.
`

// RustConst converts "neutral.spine.bg_panel" → "NEUTRAL_BG_PANEL"
// (drops the spine prefix for brevity; semantic.* keeps its role).
func RustConst(name string) (s string) {
	s = strings.ToUpper(strings.ReplaceAll(name, ".", "_"))
	s = strings.ReplaceAll(s, "NEUTRAL_SPINE_", "NEUTRAL_")
	s = strings.ReplaceAll(s, "SEMANTIC_", "")
	return
}

// GoConst converts the same name to a Go-style PascalCase identifier.
func GoConst(name string) (s string) {
	parts := strings.Split(name, ".")
	out := make([]string, 0, len(parts))
	for _, p := range parts {
		if p == "spine" || p == "semantic" {
			continue
		}
		// snake_case → PascalCase
		camel := ""
		upNext := true
		for _, ch := range p {
			if ch == '_' {
				upNext = true
				continue
			}
			if upNext {
				camel += strings.ToUpper(string(ch))
				upNext = false
			} else {
				camel += string(ch)
			}
		}
		out = append(out, camel)
	}
	if len(out) > 0 && out[0] == "Neutral" {
		// Avoid "NeutralBgPanel" → "BgPanel" feels cleaner with explicit role.
	}
	s = strings.Join(out, "")
	return
}

// RustFile renders palette_generated.rs.
func RustFile(tokens []palette.Token) (s string) {
	var sb strings.Builder
	sb.WriteString(generatedHeader)
	sb.WriteString("\nuse egui::Color32;\n\n")
	for _, t := range tokens {
		fmt.Fprintf(&sb, "/// `%s` (OKLCh L=%.3f C=%.3f h=%.1f post-clip C=%.3f)\n",
			t.Name, t.TargetL, t.TargetC, t.Hue, t.PostClipC)
		fmt.Fprintf(&sb, "pub const %s: Color32 = Color32::from_rgb(%d, %d, %d);\n\n",
			RustConst(t.Name), t.R, t.G, t.B)
	}
	s = sb.String()
	return
}

// GoFile renders palette_generated.go.
func GoFile(tokens []palette.Token) (s string) {
	var sb strings.Builder
	sb.WriteString(generatedHeader)
	sb.WriteString("\npackage styletokens\n\n")
	sb.WriteString("// RGBA8 is a gamma-encoded sRGB color with alpha = 255.\n")
	sb.WriteString("type RGBA8 struct{ R, G, B, A uint8 }\n\n")
	for _, t := range tokens {
		fmt.Fprintf(&sb, "// %s (OKLCh L=%.3f C=%.3f h=%.1f post-clip C=%.3f)\n",
			t.Name, t.TargetL, t.TargetC, t.Hue, t.PostClipC)
		fmt.Fprintf(&sb, "var %s = RGBA8{R: %d, G: %d, B: %d, A: 255}\n\n",
			GoConst(t.Name), t.R, t.G, t.B)
	}
	s = sb.String()
	return
}

// CssConst converts a token name to a kebab-case CSS custom-property name:
//
//	"neutral.spine.bg_panel"  → "--ids-bg-panel"
//	"semantic.accent.default" → "--ids-accent"
//	"semantic.error.subtle"   → "--ids-error-subtle"
//
// The default emphasis drops its suffix so the common token reads as the bare
// role (--ids-accent); --ids-<role>-subtle / --ids-<role>-strong name the two
// variants. Neutral-spine tokens drop the spine prefix (--ids-bg-panel). The
// semantic.neutral.* role stays distinct from the spine (--ids-neutral-*).
func CssConst(name string) (s string) {
	parts := strings.Split(name, ".")
	kebab := func(p string) string { return strings.ReplaceAll(p, "_", "-") }
	switch {
	case len(parts) == 3 && parts[0] == "neutral" && parts[1] == "spine":
		s = "--ids-" + kebab(parts[2])
	case len(parts) == 3 && parts[0] == "semantic" && parts[2] == "default":
		s = "--ids-" + parts[1]
	case len(parts) == 3 && parts[0] == "semantic":
		s = "--ids-" + parts[1] + "-" + parts[2]
	default:
		s = "--ids-" + kebab(strings.Join(parts, "-"))
	}
	return
}

// CssFile renders ids-palette.css — the colour custom properties consumed by
// the IDS lean classless CSS target (ADR-0076). One :root block, kebab-case
// --ids-* names per CssConst, neutral spine first then semantic roles, matching
// the Rust/Go emit order. Values are the same gamut-clipped sRGB hexes; only
// the binding surface differs.
func CssFile(tokens []palette.Token) (s string) {
	var sb strings.Builder
	sb.WriteString("/* Code generated by ./public/app/commands/designsystem colors gen — DO NOT EDIT.\n")
	sb.WriteString("   Source-of-truth: rust/imzero2/assets/colors/palette.toml\n")
	sb.WriteString("   Generator: ADR-0033 §SD5; semantic palette per ADR-0031 §SD2; CSS target per ADR-0076. */\n\n")
	sb.WriteString(":root {\n")
	wroteSemanticHeader := false
	for i, t := range tokens {
		isSpine := strings.HasPrefix(t.Name, "neutral.spine.")
		if i == 0 {
			sb.WriteString("  /* neutral spine — backgrounds, text, borders */\n")
		}
		if !isSpine && !wroteSemanticHeader {
			sb.WriteString("\n  /* semantic roles — <role> (default) / <role>-subtle / <role>-strong */\n")
			wroteSemanticHeader = true
		}
		fmt.Fprintf(&sb, "  %s: %s; /* OKLCh L=%.3f C=%.3f h=%.1f */\n",
			CssConst(t.Name), t.Hex(), t.TargetL, t.TargetC, t.Hue)
	}
	sb.WriteString("}\n")
	s = sb.String()
	return
}

// ApcaResult is the per-pair APCA grading carried into the markdown emit.
type ApcaResult struct {
	Name       string
	Category   string  // "text" | "ui"
	Lc         float64 // signed: negative = light-on-dark
	Threshold  float64 // |Lc| floor for pass
	Pass       bool
	FontPt     float64 // text only
	FontWeight int     // text only
	UIKind     string  // ui only
}

// ColorMd renders the human-readable specification. APCA is the primary
// gate per the M0b refinement; WCAG 2.1 stays as advisory.
func ColorMd(tokens []palette.Token, apcaResults []ApcaResult, contrastResults []contrast.Result) (s string) {
	var sb strings.Builder
	sb.WriteString("---\ntype: reference\n")
	sb.WriteString("audience: IDS app authors and contributors checking palette tokens\n")
	sb.WriteString("status: draft\n---\n\n")
	sb.WriteString("> **Status: draft — pre-human-review.** Generated artefact. Source: `src/rust/assets/colors/palette.toml`. ")
	sb.WriteString("Re-emit via `go run ./cmd/designsystem colors gen`.\n\n")
	sb.WriteString("# IDS color tokens (generated)\n\n")
	fmt.Fprintf(&sb, "Generated %s.\n\n", time.Now().UTC().Format("2006-01-02"))
	sb.WriteString("## Token table\n\n")
	sb.WriteString("| Token | OKLCh target (L, C, h°) | Post-clip C | sRGB |\n")
	sb.WriteString("|---|---|---|---|\n")
	for _, t := range tokens {
		fmt.Fprintf(&sb, "| `%s` | (%.3f, %.3f, %.1f) | %.3f | `%s` |\n",
			t.Name, t.TargetL, t.TargetC, t.Hue, t.PostClipC, t.Hex())
	}
	sb.WriteString("\n## APCA contrast pairs (primary gate)\n\n")
	sb.WriteString("Per the M0b refinement of [ADR-0031 §SD5](../../adr/0031-imzero2-design-system-color.md): APCA is the primary contrast metric — the SAPC S-curve algorithm Andrew Somers built for dark themes (proposed for WCAG 3.0 / Silver). Lc magnitudes; sign carries text-on-dark vs text-on-light orientation. Thresholds are size+weight-aware for text and category-driven for UI.\n\n")
	sb.WriteString("| Pair | Category | Spec | Lc | Threshold | Pass |\n")
	sb.WriteString("|---|---|---|---|---|---|\n")
	for _, r := range apcaResults {
		spec := ""
		switch r.Category {
		case "text":
			spec = fmt.Sprintf("%.0fpt/%d", r.FontPt, r.FontWeight)
		case "ui":
			spec = r.UIKind
		}
		verdict := "**fail**"
		if r.Pass {
			verdict = "pass"
		}
		fmt.Fprintf(&sb, "| `%s` | %s | %s | %+.1f | %.0f | %s |\n",
			r.Name, r.Category, spec, r.Lc, r.Threshold, verdict)
	}
	sb.WriteString("\n## WCAG 2.1 contrast pairs (advisory)\n\n")
	sb.WriteString("Kept as a secondary signal — WCAG 2.1's relative-luminance math is known to misbehave on dark themes, which is why IDS upgraded to APCA. WCAG misses warn but do not gate; treat them as a cross-check and a legacy-compliance reference, not as authority.\n\n")
	sb.WriteString("| Pair | Kind | Ratio | AA | AAA |\n")
	sb.WriteString("|---|---|---|---|---|\n")
	for _, r := range contrastResults {
		aa := "fail"
		if r.AAPass {
			aa = "pass"
		}
		aaa := "miss"
		if r.AAAPass {
			aaa = "pass"
		} else if contrast.AAAFloor(r.Kind) == 0 {
			aaa = "n/a"
		}
		fmt.Fprintf(&sb, "| `%s` | %s | %.2f:1 | %s | %s |\n",
			r.Name, r.Kind, r.Ratio, aa, aaa)
	}
	sb.WriteString("\n")
	s = sb.String()
	return
}

// IPBoundaryMd renders the IP-boundary collision log.
func IPBoundaryMd(collisions []ipboundary.Collision, sources []ipboundary.Source) (s string) {
	var sb strings.Builder
	sb.WriteString("---\ntype: reference\n")
	sb.WriteString("audience: IDS palette reviewers checking ADR-0029 §SD12 / ADR-0033 §SD7 boundary\n")
	sb.WriteString("status: draft\n---\n\n")
	sb.WriteString("> **Status: draft — pre-human-review.** Generated artefact. Re-emit via `go run ./cmd/designsystem colors gen`.\n\n")
	sb.WriteString("# IDS palette IP-boundary check (generated)\n\n")
	fmt.Fprintf(&sb, "Generated %s.\n\n", time.Now().UTC().Format("2006-01-02"))
	sb.WriteString("## Sources searched\n\n")
	sourceNames := make([]string, 0, len(sources))
	for _, s := range sources {
		sourceNames = append(sourceNames, s.System)
	}
	sort.Strings(sourceNames)
	for _, n := range sourceNames {
		for _, src := range sources {
			if src.System == n {
				fmt.Fprintf(&sb, "- **%s** — %d anchors; cached %s from %s\n",
					src.System, len(src.Anchors), src.Date, src.URL)
				break
			}
		}
	}
	sb.WriteString("\n## Hex collisions\n\n")
	if len(collisions) == 0 {
		sb.WriteString("_No hex collisions detected._\n\n")
	} else {
		sb.WriteString("A hex match alone is not a violation; the combination of (matching hex) + (role overlap) triggers a Tier 3 perturbation per [ADR-0033 §SD7](../../adr/0033-imzero2-design-system-palette-m0.md).\n\n")
		sb.WriteString("| IDS token | IDS hex | System | System token | Role-overlap |\n")
		sb.WriteString("|---|---|---|---|---|\n")
		for _, c := range collisions {
			role := ""
			if c.RoleMatches {
				role = "**yes — review**"
			} else {
				role = "no"
			}
			fmt.Fprintf(&sb, "| `%s` | `%s` | %s | `%s` | %s |\n",
				c.IDSToken, c.IDSHex, c.System, c.SystemToken, role)
		}
	}
	s = sb.String()
	return
}
