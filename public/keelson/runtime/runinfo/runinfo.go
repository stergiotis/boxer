//go:build llm_generated_opus47

package runinfo

import (
	"os"
	goruntime "runtime"
	"sync"
	"time"

	gonanoid "github.com/matoous/go-nanoid/v2"
	"github.com/rs/zerolog"

	"github.com/stergiotis/boxer/public/config/env"
	"github.com/stergiotis/boxer/public/observability/eh"
	"github.com/stergiotis/boxer/public/observability/vcs"
)

// EnvVar is the env-var name under which Init() exports the run_id so
// apps and child processes can read it without importing this package.
// Kept as a string constant for cross-language sync (Rust mirrors this
// name in src/rust/src/runinfo.rs).
const EnvVar = "PEBBLE2_RUN_ID"

// RunId is the registry-backed spec for EnvVar. The variable carries
// the spec for discovery; reads go through RunId.Lookup() so we can
// distinguish "inherited from parent" from "first-run generate".
var RunId = env.NewString(env.Spec{
	Name:        EnvVar,
	Description: "per-process run id; inherited from a parent process when set, otherwise generated by Init()",
	Category:    env.CategoryE("runinfo"),
})

// runIdLen is the length of the generated nanoid. 16 chars over the
// default alphabet (64 symbols) is ~96 bits of entropy — well in
// excess of what's needed to keep collisions astronomically unlikely
// over the audit-log lifetime, while staying short enough to read /
// type / paste comfortably.
const runIdLen = 16

// Inst is the immutable per-process run identity. Populated once via
// Init(); subsequent Get() calls return the same pointer.
type Inst struct {
	RunId        string
	Hostname     string
	Pid          int
	StartedAt    time.Time
	GoVersion    string
	VcsRevision  string
	VcsModified  bool
	VcsBuildInfo string
	ModulePath   string
}

var (
	mu        sync.Mutex
	singleton *Inst
)

// Init populates the run singleton on first call and returns it.
// Subsequent calls return the cached singleton without re-allocating;
// this makes Init safe to call from multiple entry points (e.g. the
// carousel main plus a test bootstrap) without races.
//
// Side effects on first call: sets the PEBBLE2_RUN_ID env var to the
// generated run_id (unless the env var is already set, in which case
// the inherited value is reused — useful for sub-processes that want
// to participate in a parent process's run identity).
func Init() (inst *Inst, err error) {
	mu.Lock()
	defer mu.Unlock()
	if singleton != nil {
		inst = singleton
		return
	}
	runId, set := RunId.Lookup()
	if !set {
		runId, err = gonanoid.New(runIdLen)
		if err != nil {
			err = eh.Errorf("runinfo: gonanoid.New: %w", err)
			return
		}
		setErr := os.Setenv(EnvVar, runId)
		if setErr != nil {
			err = eh.Errorf("runinfo: setenv %s: %w", EnvVar, setErr)
			return
		}
	}
	hostname, hnErr := os.Hostname()
	if hnErr != nil || hostname == "" {
		hostname = "<unknown>"
	}
	rev, modified, _ := vcs.GetVcsRevision()
	inst = &Inst{
		RunId:        runId,
		Hostname:     hostname,
		Pid:          os.Getpid(),
		StartedAt:    time.Now().UTC(),
		GoVersion:    goruntime.Version(),
		VcsRevision:  rev,
		VcsModified:  modified,
		VcsBuildInfo: vcs.BuildVersionInfo(),
		ModulePath:   vcs.ModuleInfo(),
	}
	singleton = inst
	return
}

// Get returns the previously-Init'd singleton, or an error if Init has
// not been called yet. Callers that want a guaranteed-non-nil value
// should call Init first; passing through Get is appropriate for
// downstream code that wants to fail loudly when wiring is missing.
func Get() (inst *Inst, err error) {
	mu.Lock()
	defer mu.Unlock()
	if singleton == nil {
		err = eh.Errorf("runinfo: Init has not been called")
		return
	}
	inst = singleton
	return
}

// MustGet returns the singleton or panics. Used in tight call sites
// (e.g. DockHost.Open) where missing init is a wiring bug, not a
// runtime condition to handle gracefully.
func MustGet() (inst *Inst) {
	inst, err := Get()
	if err != nil {
		panic(err)
	}
	return
}

// Reset clears the singleton — test helper only. Tests that exercise
// Init's allocation path call Reset between cases to defeat the cache.
// Not safe for concurrent use with Init or Get; tests are sequential
// per package by default.
func Reset() {
	mu.Lock()
	defer mu.Unlock()
	singleton = nil
}

// TagLogger derives a logger that includes the run_id field on every
// event. Use this once at program start to tag the global log.Logger;
// every per-app logger derived from it (via app.AppLogger) inherits
// the field through zerolog's value-semantics context chain.
func TagLogger(base zerolog.Logger, inst *Inst) (out zerolog.Logger) {
	out = base.With().Str("run_id", inst.RunId).Logger()
	return
}
