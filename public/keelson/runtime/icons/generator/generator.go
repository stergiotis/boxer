// AliasT is the optional alias metadata an upstream entry may carry —
// a deprecated kebab-case + PascalCase pair that points at the same
// codepoint as the canonical entry. ADR-0044 §SD3 emits one
// `Ph<AliasPascalName>` constant per alias, value-aliased to the
// canonical `Ph<PascalName>`.
type AliasT struct {
	Name       string `json:"name"`
	PascalName string `json:"pascal_name"`
}

// IconEntryT is the subset of @phosphor-icons/core's IconEntry that
// the generator consumes. Categories, tags, figma_category, and version
// metadata are dropped by the out-of-band tsToJson.py conversion before
// we ever see the data — see ADR-0044 §SD3.
type IconEntryT struct {
	Name       string  `json:"name"`        // kebab-case (e.g. "address-book-tabs")
	PascalName string  `json:"pascal_name"` // PascalCase (e.g. "AddressBookTabs")
	Codepoint  int32   `json:"codepoint"`   // decimal codepoint from the upstream catalogue
	Alias      *AliasT `json:"alias,omitempty"`
}

type Generator struct {
	entries []IconEntryT
}

func NewGenerator() *Generator {
	return &Generator{}
}

// Load parses the vendored phosphor-icons.json (produced from the
// upstream icons.ts via tsToJson.py) and populates the internal entries
// slice, sorted by PascalName for stable output.
func (inst *Generator) Load(jsonDoc []byte) (err error) {
	var entries []IconEntryT
	err = json.Unmarshal(jsonDoc, &entries)
	if err != nil {
		err = eh.Errorf("unable to unmarshal phosphor-icons.json: %w", err)
		return
	}
	if len(entries) == 0 {
		err = eh.Errorf("phosphor-icons.json contained zero entries")
		return
	}
	sort.Slice(entries, func(i, j int) bool {
		return entries[i].PascalName < entries[j].PascalName
	})
	inst.entries = entries
	return
}

// Entries returns the parsed catalogue (sorted by PascalName).
func (inst *Generator) Entries() []IconEntryT {
	return inst.entries
}

const banner = `// Code generated by iconsgen; DO NOT EDIT.
// Source: keelson/runtime/icons/phosphor-icons.json
//   (derived from phosphor-icons.ts via tsToJson.py;
//    icons.ts vendored from @phosphor-icons/core).
// Regenerate via: bash public/keelson/runtime/icons/generate.sh
// See ADR-0044 §SD3.
`

// GenerateGo emits the two .out.go files into the given writers.
// `pkg` is the package name to declare at the top of each file (the
// caller controls it so the generator does not have to assume the
// consumer's package path).
func (inst *Generator) GenerateGo(constsW, lookupW io.Writer, pkg string) (err error) {
	if err = inst.writeConsts(constsW, pkg); err != nil {
		err = eh.Errorf("unable to write constants file: %w", err)
		return
	}
	if err = inst.writeLookup(lookupW, pkg); err != nil {
		err = eh.Errorf("unable to write lookup file: %w", err)
		return
	}
	return
}

func (inst *Generator) writeConsts(w io.Writer, pkg string) (err error) {
	b := &strings.Builder{}
	b.WriteString(banner)
	b.WriteString("\npackage ")
	b.WriteString(pkg)
	b.WriteString("\n\n")

	b.WriteString("// Canonical Phosphor regular icons. One constant per upstream entry.\n")
	b.WriteString("const (\n")
	for _, e := range inst.entries {
		fmt.Fprintf(b, "\tPh%s = %s // %s\n",
			e.PascalName, codepointGoLiteral(e.Codepoint), e.Name)
	}
	b.WriteString(")\n")

	hasAlias := false
	for _, e := range inst.entries {
		if e.Alias != nil {
			hasAlias = true
			break
		}
	}
	if hasAlias {
		b.WriteString("\n// Deprecated aliases. Each one points at its canonical entry above; the\n")
		b.WriteString("// upstream catalogue carries these for backward compatibility with renames.\n")
		b.WriteString("const (\n")
		for _, e := range inst.entries {
			if e.Alias == nil {
				continue
			}
			fmt.Fprintf(b, "\tPh%s = Ph%s // alias of %s (was %s)\n",
				e.Alias.PascalName, e.PascalName, e.Name, e.Alias.Name)
		}
		b.WriteString(")\n")
	}

	_, err = io.WriteString(w, b.String())
	return
}

func (inst *Generator) writeLookup(w io.Writer, pkg string) (err error) {
	b := &strings.Builder{}
	b.WriteString(banner)
	b.WriteString("\npackage ")
	b.WriteString(pkg)
	b.WriteString("\n\n")

	type pair struct {
		name      string
		constExpr string
	}
	pairs := make([]pair, 0, len(inst.entries)*2)
	for _, e := range inst.entries {
		pairs = append(pairs, pair{name: e.Name, constExpr: "Ph" + e.PascalName})
		if e.Alias != nil {
			pairs = append(pairs, pair{name: e.Alias.Name, constExpr: "Ph" + e.Alias.PascalName})
		}
	}
	sort.Slice(pairs, func(i, j int) bool { return pairs[i].name < pairs[j].name })

	b.WriteString("// PhosphorNames is every kebab-case Phosphor icon name (canonical entries\n")
	b.WriteString("// and documented aliases) sorted lexically.\n")
	b.WriteString("var PhosphorNames = []string{\n")
	for _, p := range pairs {
		fmt.Fprintf(b, "\t%q,\n", p.name)
	}
	b.WriteString("}\n\n")

	b.WriteString("// PhosphorByName maps each Phosphor icon name (canonical or alias) to\n")
	b.WriteString("// the Unicode codepoint string the corresponding Ph* constant carries.\n")
	b.WriteString("var PhosphorByName = map[string]string{\n")
	for _, p := range pairs {
		fmt.Fprintf(b, "\t%q: %s,\n", p.name, p.constExpr)
	}
	b.WriteString("}\n")

	_, err = io.WriteString(w, b.String())
	return
}

// codepointGoLiteral renders an integer codepoint as the corresponding
// Go string literal. Codepoints ≤ U+FFFF emit as "\uXXXX"; codepoints
// in the supplementary plane emit as "\U00XXXXXX" (8 hex digits). Both
// are valid in Go source.
func codepointGoLiteral(cp int32) string {
	if cp <= 0xFFFF {
		return fmt.Sprintf(`"\u%04x"`, cp)
	}
	return fmt.Sprintf(`"\U%08x"`, cp)
}

// NewCommand is the urfave/cli wrapper used by cmd/iconsgen.
func NewCommand() *cli.Command {
	return &cli.Command{
		Name:  "generate",
		Usage: "regenerate phosphor.out.go and phosphor_lookup.out.go from phosphor-icons.json",
		Flags: []cli.Flag{
			&cli.StringFlag{Name: "iconsJson", Required: true, Usage: "path to vendored phosphor-icons.json"},
			&cli.StringFlag{Name: "constsOut", Required: true, Usage: "output path for the constants file"},
			&cli.StringFlag{Name: "lookupOut", Required: true, Usage: "output path for the lookup file"},
			&cli.StringFlag{Name: "package", Required: true, Usage: "Go package name for both output files"},
		},
		Action: func(c *cli.Context) (err error) {
			jsonPath := c.String("iconsJson")
			var raw []byte
			raw, err = os.ReadFile(jsonPath)
			if err != nil {
				err = eb.Build().Str("path", jsonPath).Errorf("unable to read iconsJson file: %w", err)
				return
			}
			gen := NewGenerator()
			err = gen.Load(raw)
			if err != nil {
				return
			}
			log.Info().Int("entries", len(gen.Entries())).Msg("parsed phosphor catalogue")

			constsOut := c.String("constsOut")
			lookupOut := c.String("lookupOut")
			_ = os.MkdirAll(filepath.Dir(constsOut), 0o755)
			_ = os.MkdirAll(filepath.Dir(lookupOut), 0o755)

			var constsW, lookupW *os.File
			constsW, err = os.Create(constsOut)
			if err != nil {
				err = eb.Build().Str("path", constsOut).Errorf("unable to create constsOut: %w", err)
				return
			}
			defer constsW.Close()
			lookupW, err = os.Create(lookupOut)
			if err != nil {
				err = eb.Build().Str("path", lookupOut).Errorf("unable to create lookupOut: %w", err)
				return
			}
			defer lookupW.Close()

			err = gen.GenerateGo(constsW, lookupW, c.String("package"))
			if err != nil {
				return
			}
			log.Info().Str("constsOut", constsOut).Str("lookupOut", lookupOut).Msg("emitted phosphor sources")
			return
		},
	}
}
